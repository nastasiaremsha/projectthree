    window.addEventListener('scroll', function() {
      var currentScrollPos = window.pageYOffset;
      if (currentScrollPos > 0 && !isScrolledDown) {
        brooklynDiv.classList.add('slide-in');
        brooklynDiv.classList.remove('slide-out');
        brooklynDiv.classList.add('blur')
        isScrolledDown = true;
      } else if (currentScrollPos === 0 && isScrolledDown) {
        brooklynDiv.classList.remove('slide-in');
        brooklynDiv.classList.add('slide-out');
        brooklynDiv.classList.remove('blur')
        isScrolledDown = false;
      }
    });
